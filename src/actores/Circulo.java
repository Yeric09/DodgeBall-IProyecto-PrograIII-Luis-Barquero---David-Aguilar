package actores;


public class Circulo {

    int radio;
    int x;
    int y;
    
    
    public Circulo(int radio, int x, int y) {
        this.radio = radio;
        this.x = x;
        this.y = y;
    }

    public int getRadio() {
        return radio;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setRadio(int radio) {
        this.radio = radio;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    


    

}
